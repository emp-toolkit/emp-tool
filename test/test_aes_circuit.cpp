// Test for emp-tool/circuits/aes_circuit.h:
//   - SBox vs the public AES SBox table for all 256 inputs
//   - encrypt_with_key vs FIPS-197 Appendix B test vector
//   - reports AND-gate count to verify the 6400-AND budget

#include "emp-tool/emp-tool.h"
#include "emp-tool/circuits/aes_circuit.h"
