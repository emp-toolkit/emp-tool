int main() {
	setup_clear_backend();

	// 1. SBox vs lookup table for all 256 inputs.
	// Inputs as ALICE so the clear backend doesn't constant-fold ANDs.
	int sbox_fail = 0;
	uint64_t and_at_sbox_start = backend->num_and();
	for (int v = 0; v < 256; ++v) {
		Bit in[8], out[8];
		for (int k = 0; k < 8; ++k)
			in[k] = Bit(((v >> k) & 1) != 0, ALICE);
		aes_sbox<block>(in, out);
		unsigned char got = 0;
		for (int k = 0; k < 8; ++k)
			got |= (out[k].reveal<bool>(PUBLIC) ? 1 : 0) << k;
		if (got != AES_SBOX_TABLE[v]) {
			++sbox_fail;
			if (sbox_fail < 5)
				printf("SBOX FAIL v=%02x got=%02x want=%02x\n", v, got, AES_SBOX_TABLE[v]);
		}
	}
	uint64_t and_per_sbox = (backend->num_and() - and_at_sbox_start) / 256;
	printf("SBox: %d / 256 failures, %llu ANDs/SBox (expect 32)\n",
	       sbox_fail, (unsigned long long)and_per_sbox);

	// 2. AES-128 FIPS-197 Appendix B vector.
	const unsigned char pt[16] = {
		0x32,0x43,0xf6,0xa8,0x88,0x5a,0x30,0x8d,
		0x31,0x31,0x98,0xa2,0xe0,0x37,0x07,0x34};
	const unsigned char ky[16] = {
		0x2b,0x7e,0x15,0x16,0x28,0xae,0xd2,0xa6,
		0xab,0xf7,0x15,0x88,0x09,0xcf,0x4f,0x3c};
	const unsigned char want[16] = {
		0x39,0x25,0x84,0x1d,0x02,0xdc,0x09,0xfb,
		0xdc,0x11,0x85,0x97,0x19,0x6a,0x0b,0x32};

	BitVec P_bv(128, pt, ALICE);
	BitVec K_bv(128, ky, ALICE);
	BitVec C_bv(128);

	uint64_t and_before_enc = backend->num_and();
	AES_Calculator_T<block> aes;
	aes.encrypt_with_key(P_bv.bits.data(), K_bv.bits.data(), C_bv.bits.data());
	uint64_t and_for_enc = backend->num_and() - and_before_enc;

	unsigned char got[16];
	C_bv.reveal(got, PUBLIC);

	int ok = (memcmp(got, want, 16) == 0);
	printf("AES-128 FIPS-197 vector: %s (%llu ANDs total, expect 6400)\n",
	       ok ? "OK" : "FAIL", (unsigned long long)and_for_enc);
	if (!ok) {
		printf("  got: ");  for (int i = 0; i < 16; ++i) printf("%02x", got[i]);
		printf("\n  want:"); for (int i = 0; i < 16; ++i) printf("%02x", want[i]);
		printf("\n");
	}

	// 3. Cross-validate against ParaEnc / AES_ecb_encrypt_blks (the in-clear,
	// AES-NI / SIMD reference) on random vectors.
	int xv_trials = 16, xv_fail = 0;
	srand(0xc0ffee);
	for (int t = 0; t < xv_trials; ++t) {
		unsigned char k_bytes[16], p_bytes[16];
		for (int i = 0; i < 16; ++i) {
			k_bytes[i] = (unsigned char)(rand() & 0xff);
			p_bytes[i] = (unsigned char)(rand() & 0xff);
		}

		// Reference: clear AES via AES_set_encrypt_key + AES_ecb_encrypt_blks
		// (which dispatches to ParaEnc<1,1> internally).
		alignas(16) AES_KEY ek;
		block k_blk = _mm_loadu_si128(reinterpret_cast<const __m128i *>(k_bytes));
		block p_blk = _mm_loadu_si128(reinterpret_cast<const __m128i *>(p_bytes));
		AES_set_encrypt_key(k_blk, &ek);
		AES_ecb_encrypt_blks(&p_blk, 1, &ek);
		unsigned char ct_ref[16];
		_mm_storeu_si128(reinterpret_cast<__m128i *>(ct_ref), p_blk);

		// Mine: in-circuit via AES_Calculator_T (natural byte order, LSB-first).
		BitVec Pn(128, p_bytes, ALICE);
		BitVec Kn(128, k_bytes, ALICE);
		BitVec Cn(128);
		AES_Calculator_T<block> aes_xv;
		aes_xv.encrypt_with_key(Pn.bits.data(), Kn.bits.data(), Cn.bits.data());
		unsigned char ct_mine[16];
		Cn.reveal(ct_mine, PUBLIC);

		if (memcmp(ct_mine, ct_ref, 16) != 0) {
			++xv_fail;
			if (xv_fail < 3) {
				printf("XV FAIL trial %d:\n  mine:    ", t);
				for (int i = 0; i < 16; ++i) printf("%02x", ct_mine[i]);
				printf("\n  paraenc: ");
				for (int i = 0; i < 16; ++i) printf("%02x", ct_ref[i]);
				printf("\n");
			}
		}
	}
	printf("Cross-check vs ParaEnc/AES_ecb_encrypt_blks: %d / %d failures\n",
	       xv_fail, xv_trials);

	finalize_clear_backend();
	return (sbox_fail == 0 && ok && xv_fail == 0) ? 0 : 1;
}
